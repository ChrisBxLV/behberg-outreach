/**
 * Build-time generator for `server/_generated/buildInfo.ts`.
 *
 * Uses `package.json#version` (e.g. 1.0.0 → v1.0.<commitCount>), git
 * `rev-list --count HEAD` and `rev-parse --short HEAD`, and the current time.
 * Safe fallbacks when git is missing; never blocks the build.
 *
 * Run: `node scripts/generate-build-info.mjs` (invoked from `pnpm build`).
 */

import { execFileSync } from "node:child_process";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(HERE, "..");
const PACKAGE_JSON_PATH = resolve(REPO_ROOT, "package.json");
const OUTPUT_PATH = resolve(REPO_ROOT, "server/_generated/buildInfo.ts");

function readPackageVersion() {
  try {
    const raw = readFileSync(PACKAGE_JSON_PATH, "utf8");
    const parsed = JSON.parse(raw);
    return typeof parsed.version === "string" ? parsed.version : null;
  } catch {
    return null;
  }
}

function runGit(args) {
  try {
    return execFileSync("git", args, {
      cwd: REPO_ROOT,
      stdio: ["ignore", "pipe", "ignore"],
      encoding: "utf8",
      timeout: 5_000,
    }).trim();
  } catch {
    return null;
  }
}

function readCommitCount() {
  const out = runGit(["rev-list", "--count", "HEAD"]);
  if (out == null) return null;
  const n = Number.parseInt(out, 10);
  return Number.isFinite(n) && n >= 0 ? n : null;
}

function readCommitSha() {
  const out = runGit(["rev-parse", "--short", "HEAD"]);
  return out && out.length > 0 ? out : null;
}

/** Mirrors `server/_core/buildInfoCompose.ts` `composeBuildInfo`. */
function composeBuildInfo({ packageVersion, commitCount, commitSha, now }) {
  const m = /^(\d+)\.(\d+)/.exec(String(packageVersion ?? ""));
  const prefix = m ? `v${m[1]}.${m[2]}` : "v0.0";
  const count =
    typeof commitCount === "number" && Number.isFinite(commitCount) && commitCount >= 0
      ? Math.trunc(commitCount)
      : null;
  const version = count != null ? `${prefix}.${count}` : `${prefix}.0-dev`;
  const commit =
    typeof commitSha === "string" && commitSha.trim().length > 0 ? commitSha.trim() : null;
  const buildTime = now instanceof Date ? now.toISOString() : null;
  return { version, commit, buildTime, commitCount: count };
}

function renderTsModule(info) {
  const obj = JSON.stringify(info, null, 2);
  return `// AUTO-GENERATED — do not edit by hand.
// Regenerated by \`scripts/generate-build-info.mjs\` during \`pnpm build\`.
// The committed copy is a safe placeholder used by \`pnpm check\` / \`pnpm test\`
// on fresh clones; \`pnpm build\` overwrites it with values from the local
// git repo. No secrets are written to this file.

import type { BuildInfo } from "../_core/buildInfoCompose";

export const BUILD_INFO: BuildInfo = ${obj};
`;
}

function main() {
  const info = composeBuildInfo({
    packageVersion: readPackageVersion(),
    commitCount: readCommitCount(),
    commitSha: readCommitSha(),
    now: new Date(),
  });
  mkdirSync(dirname(OUTPUT_PATH), { recursive: true });
  writeFileSync(OUTPUT_PATH, renderTsModule(info), "utf8");
  console.log(
    `[build-info] Wrote ${OUTPUT_PATH} (version=${info.version} commit=${info.commit ?? "null"} count=${
      info.commitCount ?? "null"
    })`,
  );
}

try {
  main();
} catch (err) {
  console.warn(
    "[build-info] Generation failed; placeholder kept:",
    err instanceof Error ? err.message : String(err),
  );
  process.exit(0);
}
